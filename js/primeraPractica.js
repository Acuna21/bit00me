// Función para realizar una multiplicación
function multiplicacion(num1, num2) {
    return num1 * num2;
}

// Función para realizar una resta
function resta(num1, num2) {
    return num1 - num2;
}

// Función para realizar una suma
function suma(num1, num2) {
    return num1 + num2;
}

// Función para calcular el módulo o resto de la división
function modulo(num1, num2) {
    return num1 % num2;
}

// Ejemplo de uso de las funciones
console.log("Suma: ",suma(5, 6)); 
console.log("Resta: ",resta(10, 9)); 
console.log("Mutiplicacion:", multiplicacion(4, 5)); 
console.log("Modulo:", modulo(10, 3));